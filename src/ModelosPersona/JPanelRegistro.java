    private void button_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_guardarActionPerformed
       
        if( text_documento.getText().isEmpty() ||
            text_nombre.getText().isEmpty() ||
            text_telefono.getText().isEmpty() || 
            text_email.getText().isEmpty()||
            jPassword.getText().isEmpty() || jTextApellido.getText().isEmpty()
        ){
            JOptionPane.showMessageDialog(this, "Por favor diligencie todo el formulario", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            switch(evt.getActionCommand())
            { 
                
                case "Guardar":
                    
                    persona = Personas.crear(Integer.valueOf(text_documento.getText()), text_nombre.getText(),jTextApellido.getText(),jPassword.getText(), Integer.valueOf(text_telefono.getText()), text_email.getText(),foto);
                    Metodos.crear(persona);
                    JOptionPane.showMessageDialog(this, "Su usuario ha sido creado", "Genial", JOptionPane.INFORMATION_MESSAGE);
                break;
                
            }
            
           // this.refreshTableModel();
            this.resetForm();
            this.resetPersona();
        }
    }//GEN-LAST:event_button_guardarActionPerformed

