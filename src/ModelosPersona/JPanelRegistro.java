    private void jButtonImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImagenActionPerformed
       int valor = jfcImagen.showOpenDialog(null);
            
            if(valor == JFileChooser.APPROVE_OPTION){
                File archivo = jfcImagen.getSelectedFile();
                
                Image img = obtenerImagen(archivo.getPath());
                img = redimencionarImagen(img, jlImagen.getWidth(), jlImagen.getHeight());
                mostrarImagen(img);
                
            }
    }//GEN-LAST:event_jButtonImagenActionPerformed
    /**
     * Convierte el formulario a blanco
     */
    private void resetForm() {
        text_documento.setText("");
        text_nombre.setText("");
        jTextApellido.setText("");
        jPassword.setText("");
        text_telefono.setText("");
        text_email.setText("");
        jlImagen.setText("");
    }
    /**
     * Limpia el objeto Persona
     */
    private void resetPersona() {

        persona.setDocumento(0);
        persona.setEmail("");
        persona.setNombre("");
        persona.setApellido("");
        persona.setPassword("");
        persona.setTelefono(0);
        persona.setFoto("");
        
    }
    /**
     * Obtiene la Imagen 
     * @param url
     * @return 
     */
    public Image obtenerImagen(String url){
        
        Image img = Toolkit.getDefaultToolkit().getImage(url);
        foto=url;
        System.out.println(foto);
        return img;
    }
    /**
     * Permite organizar la imagen al tamaño del Jlabel
     * @param img
     * @param ancho
     * @param alto
     * @return 
     */
    public Image redimencionarImagen(Image img, int ancho, int alto){
        return img.getScaledInstance(ancho, alto, Image.SCALE_AREA_AVERAGING);
    }
    /**
     * Muestra la imagen seleccionada
     * @param img 
     */
    public void mostrarImagen(Image img){
        ImageIcon icono = new ImageIcon(img);
        jlImagen.setIcon(icono);
    }
    
    

